package model;

public class Objeto {

	private int idObjeto;
	private int objetoIdCodigo;
	private String tipoObjeto;
	private String mensagemObjeto;
	private String informacaoObjeto;
	
	
	
	public int getIdObjeto() {
		return idObjeto;
	}
	public void setIdObjeto(int idObjeto) {
		this.idObjeto = idObjeto;
	}
	public int getObjetoIdCodigo() {
		return objetoIdCodigo;
	}
	public void setObjetoIdCodigo(int objetoIdCodigo) {
		this.objetoIdCodigo = objetoIdCodigo;
	}
	public String getTipoObjeto() {
		return tipoObjeto;
	}
	public void setTipoObjeto(String tipoObjeto) {
		this.tipoObjeto = tipoObjeto;
	}
	public String getMensagemObjeto() {
		return mensagemObjeto;
	}
	public void setMensagemObjeto(String mensagemObjeto) {
		this.mensagemObjeto = mensagemObjeto;
	}
	public String getInformacaoObjeto() {
		return informacaoObjeto;
	}
	public void setInformacaoObjeto(String informacaoObjeto) {
		this.informacaoObjeto = informacaoObjeto;
	}
	
	
}
